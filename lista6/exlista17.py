# contar o número de ocorrências de um valor

produtos = ["bolo", "suco", "salgado", "bolo", "picolé", "bolo", "pastel", "refrigerante", "suco"]

print(f"Bolos vendidos = {produtos.count('bolo')}")
print(f"Sucos vendidos = {produtos.count('suco')}")
print(f"Salgados vendidos = {produtos.count('salgado')}")
